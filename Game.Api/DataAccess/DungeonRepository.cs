        private static List<Dungeon> dungeons;

        public DungeonRepository()
        {
            dungeons = new List<Dungeon>();

            var dungeonMap = new List<MapCell>();

            for (var y = 0; y < map.Length; y++)
            {
                for (var x = 0; x < map[y].Length; x++)
                {
                    var type = map[y][x];
                    if(type == 0)
                    {
                        type = 248;
                    }
                    else if(type == 1)
                    {
                        type = 96;
                    }

                    dungeonMap.Add(new MapCell
                    {
                        X = x,
                        Y = y,
                        Type = type,
                        IsWalkable = map[y][x] == 0,
                        IsTransparent = map[y][x] != 1
                    });
                }
            }

            var dungeonUnits = new List<Unit>();
            dungeonUnits.Add(new Unit
            {
                Id = 95,
                UnitType = 1,
                X = 6,
                Y = 6,
                Character = new Character { Id = 1, Armor = 0, Damage = 10, Health = 100, Speed = 1, Name = "archer", AttackRange = 3, WatchRange = 5, AttackSpeed = 2000 }
            });

            var dungeon = new Dungeon
            {
                Id = 1,
                Name = "Test",
                MaxPlayersNumber = 2,
                Map = dungeonMap,
                Units = dungeonUnits,
                Height = map.Length,
                Width = map[0].Length,
                OriginPositionX = 1,
                OriginPositionY = 1
            };

            dungeons.Add(dungeon);
        }

        public Dungeon GetById(long id)
        {
            return dungeons.FirstOrDefault(it => it.Id == id)?.Clone();
        }

        public Dungeon GetByName(string name)
        {
            return dungeons.FirstOrDefault(it => it.Name == name)?.Clone();
        }
